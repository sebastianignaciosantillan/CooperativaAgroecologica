    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Productor productor = new Productor();
        Producto producto = new Producto();
        String  idProductor= txtIdProducto.getText(); //usare el  id del productor para traerme el objeto completo de productor que me lo pide como parametro el producto.
        int idP = Integer.parseInt(idProductor); //casteo porque siempre se trae un tipo String desde el formulario

        productor =   control.traerUnProductor(idP); // una vez que se trae mediante el idProductor se envia este objeto como parametro al producto para mantener su relacion
        String nombre = txtNombre.getText();
        String descripcion = txtDescripcion.getText();
        //Para obtener la fecha de manera mas profesional se opto por esta manera
        DateFormat dateFormat = new SimpleDateFormat("dd-MM-yyyy", Locale.getDefault());
        String fechaIngreso = dateFormat.format(txtFechaIngreso.getDate());
        
        String observaciones = txtAObservacion.getText();
        int cantidad = slider.getValue(); //valor del slider obtenido
        String bultos = (String) cbBultos.getSelectedItem();
         int bultosInt = Integer.parseInt(bultos);
        // asigno los valores que me traigo desde el formulario
        producto.setNombre(nombre);
        producto.setDescripcion(descripcion);
        producto.setObservaciones(observaciones);
        producto.setFechaIngreso(fechaIngreso);
        producto.setBultos(bultosInt);
        producto.setCantidad(cantidad);
        //necesito enviar el productor como un set
        producto.setProductor(productor);
        control.crearProducto(producto); 
        //control.crearProductor(producto);
        JOptionPane.showMessageDialog(null, "Guardado con Ã©xito");
        limpiarPantalla();
          this.dispose(); //cierra ventana actual
    }//GEN-LAST:event_btnGuardarActionPerformed

