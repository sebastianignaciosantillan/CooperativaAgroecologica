    private void cargarTabla() {
       
        //Definir el modelo que tendra la tabla
       DefaultTableModel modelotabla = new DefaultTableModel() {
           //filas y columnas no seran editable
           @Override
           public boolean isCellEditable (int row, int column ) {
               return  false;
           }
       };
       //establece los nombres de la columna
       String titulos[] =  {"id Prod", "Nombre", "Puesto","Telefono","Localidad", "Tipo","Fecha Ingreso"};
       modelotabla.setColumnIdentifiers(titulos);
       //carga de los datos de la base de datos
       List<Productor> listaProductor = control.traerProductores();
        
       //traer lista y mostrar cada uno de los elementos en la tabla:
       if(listaProductor !=null){
           
           for(Productor aux : listaProductor){
               Object[] objeto = {aux.getIdproductor(), aux.getNombre(), aux.getPuesto(), aux.getTelefono(), aux.getTipo(), aux.getLocalidad(), aux.getFechaIngreso() };
                modelotabla.addRow(objeto);
           }
       }
       tablaProductores.setModel(modelotabla); // a mi tabla le asigno la tabla que cree
    
    }

