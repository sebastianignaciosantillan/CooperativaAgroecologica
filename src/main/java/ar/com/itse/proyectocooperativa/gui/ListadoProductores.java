// Método para actualizar la tabla de Productores
private void actualizarTablaProductores(List<Productor> productores) {
    DefaultTableModel model = new DefaultTableModel();
    model.setColumnIdentifiers(new String[]{"Id Productor", "Nombre", "Puesto", "Teléfono", "Localidad", "Tipo", "Fecha Ingreso"});

    for (Productor productor : productores) {
        model.addRow(new Object[]{
            productor.getIdproductor(),
            productor.getNombre(),
            productor.getPuesto(),
            productor.getTelefono(),
            productor.getLocalidad(),
            productor.getTipo(),
            productor.getFechaIngreso()
        });
    }

    tablaProductores.setModel(model);

    }//GEN-LAST:event_btnBuscarPActionPerformed

