    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        if(tablaProductores.getRowCount()>0) { 
            if(tablaProductores.getSelectedRow()!= -1 ){ 
                int num_productor = Integer.parseInt(String.valueOf(tablaProductores.getValueAt(tablaProductores.getSelectedRow(), 0))); //se trae num del indice de la tabla
                control.eliminarProductor(num_productor);
                mostrarMensaje("Borrado con exito");
                cargarTabla();
            }
            else{
                mostrarMensaje("No selecciono ningun productor");
            }
       }
        else {
            mostrarMensaje("No hay nada para eliminar en la tabla");
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

